"""
Elegantly prints the current time step on the Python console.
"""

# =============================================================================
def time_step(ts):
# -----------------------------------------------------------------------------
    """
    Args:
      ts: Time step.

    Returns:
      None!
    """

    print("+====================+")
    print("|                    |")
    print("| Time step = " +"%6d" %ts + " |")
    print("|                    |")
    print("+====================+")

    return  # end of function
